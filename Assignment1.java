// main class
class Assignment1 {
  // main method
  public static void main(String[] args) {

    // declaring variables a, b, c with , , 
    int a = 10, b = 20, c = 30;

    // initializing sum, product, sub
    int sum = a + b + c;
    int product = a * b * c;
    int sub = a - b - c;

    // Sum of 3 variables
    System.out.println("Sum of 3 variables = " +sum);
    // product of 3 variables
    System.out.println("Product of 3 variables = " +product);
    // Sub of 3 variables
    System.out.println("Sub of 3 variables = " +sub);
    }
}